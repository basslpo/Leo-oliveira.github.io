function fizzBuzz(start, end) {

  for (let n = 1; start <= end; n++) {
    let output = "";
    if (n % 3 == 0) output += "fizz";
    if (n % 5 == 0) output += "buzz";
    if (n === end) {
      console.log("fizzbuzz")
      return;
    }
    console.log(output || n);
  }
};



