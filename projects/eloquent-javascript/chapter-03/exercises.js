// countChars //////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function countChars(string, letter) {
        let charArr = string.split('')
        let count = 0;
          for ( let i = 0; i < charArr.length; i++) { 
            if ( charArr[i] === letter) {
              count++
          }

    }
    return count;
}

